package com.example.shyam.bugtrackingsystem;



public class ComplaintModel {
	

	int id;
	String compltype;
	String compldesc;
	String status;
	

	
	
	
	

	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCompltype() {
		return compltype;
	}
	public void setCompltype(String compltype) {
		this.compltype = compltype;
	}
	public String getCompldesc() {
		return compldesc;
	}
	public void setCompldesc(String compldesc) {
		this.compldesc = compldesc;
	}
	
	

}
